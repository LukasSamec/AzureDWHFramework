CREATE TABLE [dwh].[F_SalesOrderHeader] (
    [SalesOrderHeaderID] INT    IDENTITY (1, 1) NOT NULL,
    [SalesOrderID]       INT    NOT NULL,
    [OrderDateID]        INT    NOT NULL,
    [DueDateID]          INT    NOT NULL,
    [ShipDateID]         INT    NOT NULL,
    [SalesPersonID]      INT    NULL,
    [BillAddressID]      INT    NOT NULL,
    [ShipAddressID]      INT    NOT NULL,
    [SubTotal]           MONEY  NOT NULL,
    [TaxAmt]             MONEY  NOT NULL,
    [Freight]            MONEY  NOT NULL,
    [InsertedETLLogID]   BIGINT NOT NULL,
