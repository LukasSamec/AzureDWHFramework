    FOREIGN KEY ([ShipAddressID]) REFERENCES [dwh].[D_Address] ([AddressID]),
    FOREIGN KEY ([ShipDateID]) REFERENCES [dwh].[D_Date] ([DateID]),
    FOREIGN KEY ([UpdatedETLLogID]) REFERENCES [log].[ETLLog] ([ETLLogID])
);

